export type TKey = keyof typeof translations.pt;

// Mapeamento locale → moeda padrão
const LOCALE_CURRENCY: Record<Locale, string> = {
  pt: "EUR",
  en: "USD",
  fr: "EUR",
  es: "EUR",
};

export function getLocale(): Locale {
  try {
    const jar = cookies();
    const cookieLang = jar.get("LC")?.value;
    if (cookieLang === "en" || cookieLang === "fr" || cookieLang === "es" || cookieLang === "pt") {
      return cookieLang;
    }
    // Detecta idioma pelo browser Accept-Language
    const hdrs = headers();
    const accept = hdrs.get("accept-language") ?? "";
    const lang = accept.split(",")[0].split("-")[0].toLowerCase();
    if (lang === "en") return "en";
    if (lang === "fr") return "fr";
    if (lang === "es") return "es";
  } catch {}
  return "pt";
}

export function getLocaleCurrency(locale?: Locale): string {
  try {
    const jar = cookies();
    const cookieCur = jar.get("CUR")?.value;
    if (cookieCur === "EUR" || cookieCur === "USD" || cookieCur === "AOA") return cookieCur;
  } catch {}
  const l = locale ?? getLocale();
  return LOCALE_CURRENCY[l] ?? "EUR";
}

export function localeCurrencySymbol(locale?: Locale): string {
  const cur = getLocaleCurrency(locale);
  return cur === "AOA" ? "Kz" : cur === "USD" ? "$" : "€";
}

export function t(key: TKey, locale?: Locale): string {
  const l = locale ?? getLocale();
  return (translations[l] as Record<string, string>)[key] ?? (translations.pt as Record<string, string>)[key] ?? key;
}
