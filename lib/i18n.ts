export type TKey = keyof typeof translations.pt;

export function getLocale(): Locale {
  try {
    // Cookie override takes priority (set by user clicking the language switcher)
    const jar = cookies();
    const cookieLang = jar.get("LC")?.value;
    if (cookieLang === "en" || cookieLang === "fr" || cookieLang === "es" || cookieLang === "pt") {
      return cookieLang;
    }
    // Fall back to browser Accept-Language
    const hdrs = headers();
    const accept = hdrs.get("accept-language") ?? "";
    const lang = accept.split(",")[0].split("-")[0].toLowerCase();
    if (lang === "en") return "en";
    if (lang === "fr") return "fr";
    if (lang === "es") return "es";
  } catch {}
  return "pt";
}

export function t(key: TKey, locale?: Locale): string {
  const l = locale ?? getLocale();
  return (translations[l] as Record<string, string>)[key] ?? (translations.pt as Record<string, string>)[key] ?? key;
}
